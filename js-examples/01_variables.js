// Variablen können über mehrere Wege in JavaScript angelegt werden
// Der erste Weg ist var:
var variable = 0;
// var steht einfach für Variable und ist nicht präzise und veraltet.
// Es wurde abgelöst durch die zwei folgenden Arten:
const konstante = 0;
// const steht für Konstanten und zeigt Variablen an, deren Wert sich
// in der Ausführung des Programmes nicht verändert.
// Als Komplementär dazu gibt es als Indikator für sich verändernde
// Variablen das keyword 'let':
let variable2 = 0;
// Lasse die variable2 den Wert 0 haben. Diese kann man wie bereits
// erwähnt verändern:
variable2 = 69;
