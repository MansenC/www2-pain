// In JavaScript gibt es keine expliziten Typen. Typen werden von der
// Sprache automatisch erkannt. Jedoch gibt es insgesamt 6 Typen,
// die wir während der Programmierung verwenden können:
// - Booleans für Wahr/Falsch-Werte
// - Strings für Texte
// - Numbers für Zahlen
// - Objects für Objekte, die Variablen enthalten können
// - Arrays für eine Ansammlung von Werten
// - Functions für Funktionen, die wir aufrufen können
// Hier eine Auflistung der ersten drei Typen:

// Booleans:
const trueValue = true;
const falseValue = false;

// Numbers:
const integerValue = -69;
const floatingValue = 420.69;
// Zahlen werden immer als Kommazahlen betrachtet.

// Strings können auf drei Arten angelegt werden:
const doubleQuotes = "Ich bin ein Text";
const singleQuotes = 'Ich auch!';
// Es besteht kein Unterschied zwischen den beiden.

const specialText = `Diese Art von Anführungszeichen kann für Texte
über mehrere Zeilen genutzt werden, und kann Variablen einbinden: ${trueValue}`;

